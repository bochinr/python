# 创建一个字典d1。
d1 = {"中国": "北京", "韩国": "首尔", "英国": "伦敦", "美国": "华盛顿"}
print(d1)
# d2键的数据类型为元组。
d2 = {(1, "中国"): "北京", (2, "英国"): "伦敦", (3, "美国"): "华盛顿"}
print(d2)
# 创建一个空字典d3。
d3 = {}
print(d3)
# 用dict()函数创建一个字典d4。
d4 = dict({"日本": "东京"})
print(d4)
# d5键的数据类型为列表，则会报错。因为键是不可变的，而列表是可变的数据类型。
d5 = {[1, "中国"]: "北京", [2, "英国"]: "伦敦", [3, "美国"]: "华盛顿"}
print(d5)
